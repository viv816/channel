package com.beans.esb.util;

public class BFTException extends RuntimeException {
	/**
	 * 
	 */
	private static final long serialVersionUID = -8741390597638777104L;

	public BFTException() {
		super();
	}

	public BFTException(String message, Throwable cause) {
		super(message, cause);
	}

	public BFTException(String message) {
		super(message);
	}

	public BFTException(Throwable cause) {
		super(cause);
	}
	
}
